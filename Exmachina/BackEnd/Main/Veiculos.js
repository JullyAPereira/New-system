class Veiculo {
    constructor(chassi, placa, marca, modelo, anoFabricacao, anoModelo, cor, valor) {
        this.chassi = chassi; // Número do chassi
        this.placa = placa; // Placa
        this.marca = marca; // Marca
        this.modelo = modelo; // Modelo
        this.anoFabricacao = anoFabricacao; // Ano de fabricação
        this.anoModelo = anoModelo; // Ano do modelo
        this.cor = cor; // Cor
        this.valor = valor; // Valor
    }
}